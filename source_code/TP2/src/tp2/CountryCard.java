    public void setCard() {
        labelName.setText(country.getName());
        labelTotalVolc.setText(String.valueOf(country.getHoloceneVolc()));
        
        // Read the image
        Image image;
        try {
            image = ImageIO.read(new File(country.getFlagPath()));
            image = image.getScaledInstance(133, 88, Image.SCALE_SMOOTH);
            // Display image
            labelImage.setIcon(new ImageIcon(image));
        } catch (IOException ex) {
            System.out.println(ex.getMessage()+" Because dist and src have different uploads folder");
        }
    }


    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Confirmation prompt
        int response = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus?", "confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        // Action
        if (response == JOptionPane.YES_OPTION) {
            try {
                // Delete row from database
                String query = "DELETE FROM country WHERE id_country='"+country.getId()+"';";
                db.getStatement().executeUpdate(query);

                // Delete the image file in the uploads folder
                File imageFile = new File(country.getFlagPath());
                if (imageFile.exists()) {
                    if (imageFile.delete()) {
                        System.out.println("Image file deleted");
                    }
                }

                // Reset list from parent container
                MainFrame parent = (MainFrame) this.getTopLevelAncestor();
                if (parent != null) {
                    parent.setPanel();
                }

                // Show Information
                System.out.println("Delete country success");
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(null, "Tidak dapat menghapus data yang dikaitkan dengan volcano", "Error", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

